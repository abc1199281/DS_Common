namespace DS_Common {

	//--------------------------------------------------------------------------------------------
	template<class T>
	void InsertionSort(T* arr, const int n)
	{
		if (n <= 1)return;
		for (int i = 1; i <n; i++)
		{
			T key = arr[i];
			int j = i - 1;
			while (key < arr[j] && j >= 0)
			{
				arr[j + 1] = arr[j];
				j--;
			}
			arr[j + 1] = key;
		}
	}

	//--------------------------------------------------------------------------------------------	
}
